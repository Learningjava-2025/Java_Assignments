//Take 2 numbers as input and print the largest number.

import java.util.Scanner;
public class Largest {
    public static void main(String[] args) {
        Scanner num = new Scanner(System.in);
        int x = num.nextInt();
        int y = num.nextInt();
        System.out.println(Math.max(x,y));
    }
}
